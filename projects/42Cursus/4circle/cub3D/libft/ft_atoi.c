#include "libft.h"

int	ft_atoi(const char *str)
{
	int	res;

	while ((9 <= *str && *str <= 13) || *str == 32)
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			return (-1);
		str++;
	}
	if (*str == '\0')
		return (-1);
	res = 0;
	while ('0' <= *str && *str <= '9')
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	if (*str != '\0')
		return (-1);
	if (res > 255)
		return (-1);
	return (res);
}
